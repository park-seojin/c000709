#include <unistd.h>

void	ft_print_comb2(void)
{
	char	num[6];
	int		n;
	int		n2;

	num[2] = ' ';
	n = 0;
	while (n < 99)
	{
		num[0] = n / 10 % 10 + '0';
		num[1] = n % 10 + '0';
		n2 = n + 1;
		while (n2 < 100)
		{
			num[3] = n2 / 10 % 10 + '0';
			num[4] = n2 % 10 + '0';
			write(1, num, 5);
			if (n == 98 && n2 == 99)
				break ;
			write(1, ", ", 1);
			n2++;
		}
		n2 = 0;
		n++;
	}
}
