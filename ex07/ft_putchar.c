#include <unistd.h>

int	check_neg(int *nb)
{
	int min;

	min = -2147483648;
	if (!(*nb))
	{
		write(1, "0", 1);
		return (1);
	}
	if (*nb < 0)
	{
		write(1, "-", 1);
		if(*nb == min)
		{
		write(1, "2147483648", 10);
		return (1);
		}
	*nb *= -1;
	}
	return (0);
}


